#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	int equ;

	if (s1 && s2)
	{
		equ = ft_strncmp(s1, s2, n);
		if (equ == 0)
			return (1);
	}
	return (0);
}
